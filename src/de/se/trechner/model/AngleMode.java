package de.se.trechner.model;

/**
 * Dieser enum dient der Typsicherheit bei der Moduswahl.
 * Die drei Modi stehen für verschieden Winkelmaß:
 * DEG - Gradmaß
 * RAD - Bogenmaß
 * GRAD - geodätisches Winkelmaß (gon)
 * 
 * @author wojke_n
 * @version 2017-12-10
 */
public enum AngleMode {
	DEG,
	RAD,
	GRAD;
}